package old;

import java.util.ArrayList;

public class Request {
	ArrayList<Integer> shipSizes;
	int boardWidth;
	int boardHeight;
	int clientManagerID;
	
	public Request(ArrayList<Integer> shipSizes, int boardWidth, int boardHeight, int clientManagerID) {
		this.shipSizes = shipSizes;
		this.boardWidth = boardWidth;
		this.boardHeight = boardHeight;
		this.clientManagerID = clientManagerID;
	}
}
